<?php

namespace SMA\AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Jurusan
 */
class Jurusan
{
    /**
     * @var string
     */
    private $jurusan;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set jurusan
     *
     * @param string $jurusan
     * @return Jurusan
     */
    public function setJurusan($jurusan)
    {
        $this->jurusan = $jurusan;

        return $this;
    }

    /**
     * Get jurusan
     *
     * @return string 
     */
    public function getJurusan()
    {
        return $this->jurusan;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
}
